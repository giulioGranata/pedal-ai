// 3 voci: Dashboard, Chat, Settings
const navItems = [
  { href: '/', label: 'Dashboard', icon: IconDashboard },
  { href: '/chat', label: 'Chat', icon: IconChat },
  { href: '/settings', label: 'Settings', icon: IconSettings },
];

// Visibile solo su mobile (md:hidden), fissa in basso
export default function BottomNav() {
  const pathname = usePathname();

  return (
    <nav className="md:hidden fixed bottom-0 left-0 right-0 z-50 flex border-t border-gray-200 dark:border-gray-800 bg-white dark:bg-gray-950">
      {navItems.map(({ href, label, icon: Icon }) => {
        const isActive = href === '/' ? pathname === '/' : pathname.startsWith(href);
        return (
          <Link
            key={href}
            href={href}
            className={`
              flex-1 flex flex-col items-center justify-center gap-0.5 py-2.5
              text-xs font-medium transition-colors duration-150
              ${isActive
                ? 'text-[#1D9E75]'
                : 'text-gray-500 dark:text-gray-400 hover:text-gray-900 dark:hover:text-gray-100'
              }
            `}
          >
            <Icon />
            <span>{label}</span>
          </Link>
        );
      })}
    </nav>
  );
}
